      if (1 & (xGT (COREHALFWORD (mGENERATExFORCE_ADDRESSxTEMP), 0)))
        // IF SHIFT_AMOUNT > 0 THEN (5030)
        if (1 & (xGT (BYTE0 (mGENERATExFORCE_ADDRESSxSHIFT_AMOUNT), 0)))
          // DO; (5031)
          {
          rs10:;
            // IF USAGE(TEMP) > 1 THEN (5032)
            if (1
                & (xGT (COREHALFWORD (
                            mUSAGE
                            + 2 * COREHALFWORD (mGENERATExFORCE_ADDRESSxTEMP)),
                        1)))
              // CALL EMITP(SRA, TEMP, 0, SHCOUNT, SHIFT_AMOUNT); (5033)
              {
                putBITp (16, mGENERATExEMITPxINST, getBIT (8, mSRA));
                putBITp (16, mGENERATExEMITPxXREG,
                         getBIT (16, mGENERATExFORCE_ADDRESSxTEMP));
                putBITp (16, mGENERATExEMITPxINDEX,
                         fixedToBit (32, (int32_t)(0)));
                putBITp (16, mGENERATExEMITPxFLAG, getBIT (8, mSHCOUNT));
                putBITp (16, mGENERATExEMITPxPTR,
                         getBIT (8, mGENERATExFORCE_ADDRESSxSHIFT_AMOUNT));
                GENERATExEMITP (0);
              }
            // ELSE (5034)
            else
              // CALL UNRECOGNIZABLE(TEMP); (5035)
              {
                putBITp (16, mGENERATExUNRECOGNIZABLExR,
                         getBIT (16, mGENERATExFORCE_ADDRESSxTEMP));
                GENERATExUNRECOGNIZABLE (0);
              }
          es10:;
          } // End of DO block
// SET_R: (5036)
SET_R:
  // CALL UNRECOGNIZABLE(R); (5037)
  {
    putBITp (16, mGENERATExUNRECOGNIZABLExR,
             getBIT (16, mGENERATExFORCE_ADDRESSxR));
    GENERATExUNRECOGNIZABLE (0);
  }
  // IF FLAG THEN (5038)
  if (1 & (bitToFixed (getBIT (1, mGENERATExFORCE_ADDRESSxFLAG))))
    // USAGE(R) = 2; (5039)
    {
      int32_t numberRHS = (int32_t)(2);
      descriptor_t *bitRHS;
      bitRHS = fixedToBit (32, (int32_t)(numberRHS));
      putBIT (16, mUSAGE + 2 * (COREHALFWORD (mGENERATExFORCE_ADDRESSxR)),
              bitRHS);
      bitRHS->inUse = 0;
    }
  // R_TYPE(R) = DINTEGER; (5040)
  {
    descriptor_t *bitRHS = getBIT (8, mDINTEGER);
    putBIT (8, mR_TYPE + 1 * (COREHALFWORD (mGENERATExFORCE_ADDRESSxR)),
            bitRHS);
    bitRHS->inUse = 0;
  }
  // USAGE_LINE(R) = 1; (5041)
  {
    int32_t numberRHS = (int32_t)(1);
    descriptor_t *bitRHS;
    bitRHS = fixedToBit (32, (int32_t)(numberRHS));
    putBIT (16, mUSAGE_LINE + 2 * (COREHALFWORD (mGENERATExFORCE_ADDRESSxR)),
            bitRHS);
    bitRHS->inUse = 0;
  }
  // REG(OP) = R; (5042)
  {
    descriptor_t *bitRHS = getBIT (16, mGENERATExFORCE_ADDRESSxR);
    putBIT (16,
            getFIXED (mIND_STACK)
                + 73 * (COREHALFWORD (mGENERATExFORCE_ADDRESSxOP)) + 46
                + 2 * (0),
            bitRHS);
    bitRHS->inUse = 0;
  }
  // FLAG, FOR_NAME, BY_NAME = FALSE; (5043)
  {
    int32_t numberRHS = (int32_t)(0);
    descriptor_t *bitRHS;
    bitRHS = fixedToBit (32, (int32_t)(numberRHS));
    putBIT (1, mGENERATExFORCE_ADDRESSxFLAG, bitRHS);
    bitRHS = fixedToBit (32, (int32_t)(numberRHS));
    putBIT (1, mGENERATExFORCE_ADDRESSxFOR_NAME, bitRHS);
    bitRHS = fixedToBit (32, (int32_t)(numberRHS));
    putBIT (1, mGENERATExFORCE_ADDRESSxBY_NAME, bitRHS);
    bitRHS->inUse = 0;
  }
  // TARGET_R = -1; (5044)
  {
    int32_t numberRHS = (int32_t)(-1);
    descriptor_t *bitRHS;
    bitRHS = fixedToBit (32, (int32_t)(numberRHS));
    putBIT (16, mTARGET_R, bitRHS);
    bitRHS->inUse = 0;
  }
  // CALL CHECK_LINKREG(R); (5045)
  {
    putBITp (16, mGENERATExCHECK_LINKREGxR,
             getBIT (16, mGENERATExFORCE_ADDRESSxR));
    GENERATExCHECK_LINKREG (0);
  }
  // NR_DEREF(OP)=NR_DEREF_TMP(OP); (5046)
  {
    descriptor_t *bitRHS
        = getBIT (1, getFIXED (mIND_STACK)
                         + 73 * (COREHALFWORD (mGENERATExFORCE_ADDRESSxOP))
                         + 61 + 1 * (0));
    putBIT (8,
            getFIXED (mIND_STACK)
                + 73 * (COREHALFWORD (mGENERATExFORCE_ADDRESSxOP)) + 60
                + 1 * (0),
            bitRHS);
    bitRHS->inUse = 0;
  }
  // NR_DEREF_TMP(OP)=FALSE; (5047)
  {
    int32_t numberRHS = (int32_t)(0);
    putBIT (1,
            getFIXED (mIND_STACK)
                + 73 * (COREHALFWORD (mGENERATExFORCE_ADDRESSxOP)) + 61
                + 1 * (0),
            fixedToBit (1, numberRHS));
  }
  // RETURN R; (5048)
  {
    reentryGuard = 0;
    return COREHALFWORD (mGENERATExFORCE_ADDRESSxR);
  }
