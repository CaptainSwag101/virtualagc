            putFIXED (mGENERATExSET_USAGExRVAR,
                      COREHALFWORD (
                          getFIXED (mIND_STACK)
                          + 73 * (COREHALFWORD (mGENERATExCHECKPOINT_REGxPTR))
                          + 40 + 2 * (0)));
            GENERATExSET_USAGE (0);
