{

  // Setup for MONITOR(6), MONITOR(7), MONITOR(21).  Initially,
  // entire physical memory is a single pre-allocated block.
  MONITOR6a (USER_MEMORY, PHYSICAL_MEMORY_LIMIT, 0);
  MONITOR6a (PRIVATE_MEMORY, PRIVATE_MEMORY_SIZE, 0);

  if (parseCommandLine (argc, argv))
    exit (0);

  static int reentryGuard = 0;
  reentryGuard = guardReentry (reentryGuard, "main");
  int setjmpInitialize = 1;
  if (setjmpInitialize)
    {
      goto BOMB_OUT;
    setjmpInitialized:
      setjmpInitialize = 0;
    }
// MAIN_PROGRAM: (0)
MAIN_PROGRAM:
  // CALL INITIALIZE; (1)
  INITIALIZE (0);
  // CALL PROCESS_HALMAT; (2)
  PROCESS_HALMAT (0);
  // CALL SORT_VPTRS; (3)
  SORT_VPTRS (0);
  // IF (HALMAT_DUMP & HMAT_OPT) THEN (4)
  if (1 & (xAND (BYTE0 (mHALMAT_DUMP), BYTE0 (mHMAT_OPT))))
    // CALL DUMP_STMT_HALMAT; (5)
    DUMP_STMT_HALMAT (0);
  // IF VMEM_DUMP THEN (6)
  if (1 & (bitToFixed (getBIT (8, mVMEM_DUMP))))
    // CALL DUMP_ALL(FORMATTED_DUMP); (7)
    {
      putBITp (16, mDUMP_ALLxFORMAT, getBIT (8, mFORMATTED_DUMP));
      DUMP_ALL (0);
    }
  // IF DONT_LINK THEN (8)
  if (1 & (bitToFixed (getBIT (8, mDONT_LINK))))
    // RETURN 0; (9)
    {
      RECORD_LINK (0);
    }
  // ELSE (10)
  else
    // DO; (11)
    {
    rs1:;
      // RECORD_SEAL(SYM_ADD); (12)
      {
        putFIXED (m_RECORD_SEALxDOPE, ADDR ("SYM_ADD", 0x80000000, NULL, 0));
        _RECORD_SEAL (0);
      }
      // CALL RECORD_LINK; (13)
      RECORD_LINK (0);
    es1:;
    } // End of DO block
  // BOMB_OUT: (14)
  if (0)
    {
    BOMB_OUT:
      if (setjmpInitialize)
        {
          if (!setjmp (jbBOMB_OUT))
            goto setjmpInitialized;
        }
    }
  // IF MAX_SEVERITY >= 2 THEN (15)
  if (1 & (xGE (COREHALFWORD (mMAX_SEVERITY), 2)))
    // CALL DOWNGRADE_SUMMARY; (16)
    DOWNGRADE_SUMMARY (0);
  // OUTPUT = '***  COMPILATION ABANDONED  ***'; (17)
  {
    descriptor_t *stringRHS;
    stringRHS = cToDescriptor (NULL, "***  COMPILATION ABANDONED  ***");
    OUTPUT (0, stringRHS);
    stringRHS->inUse = 0;
  }
  // CALL DUMP_ALL(FORMATTED_DUMP); (18)
  {
    putBITp (16, mDUMP_ALLxFORMAT, getBIT (8, mFORMATTED_DUMP));
    DUMP_ALL (0);
  }
  // RETURN COMMON_RETURN_CODE; (19)
  {
    RECORD_LINK (0);
  }
// NO_CORE: (20)
NO_CORE:
  // OUTPUT = '***  COMPILATION ABANDONED -- INSUFFICIENT CORE  ***'; (21)
  {
    descriptor_t *stringRHS;
    stringRHS = cToDescriptor (
        NULL, "***  COMPILATION ABANDONED -- INSUFFICIENT CORE  ***");
    OUTPUT (0, stringRHS);
    stringRHS->inUse = 0;
  }
  // CALL DUMP_ALL(FORMATTED_DUMP); (22)
  {
    putBITp (16, mDUMP_ALLxFORMAT, getBIT (8, mFORMATTED_DUMP));
    DUMP_ALL (0);
  }
  // RETURN 20; (23)
  {
    RECORD_LINK (0);
  }

  RECORD_LINK (0);
  {
    reentryGuard = 0;
    return 0;
  } // Just in case ...
}
